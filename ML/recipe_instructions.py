def get_recipe_instructions(dish_name):
    """
    Get detailed cooking instructions for a dish
    Returns instructions dict or None if not found
    """
    # Try exact match first
    if dish_name in RECIPE_INSTRUCTIONS:
        return RECIPE_INSTRUCTIONS[dish_name]
    
    # Try case-insensitive match
    dish_name_lower = dish_name.lower()
    for key in RECIPE_INSTRUCTIONS:
        if key.lower() == dish_name_lower:
            return RECIPE_INSTRUCTIONS[key]
    
    return None
