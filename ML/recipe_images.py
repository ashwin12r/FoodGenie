def get_recipe_image(dish_name):
    """
    Get image URL for a dish
    Returns image URL or default if not found
    """
    # Try exact match first
    if dish_name in RECIPE_IMAGES:
        return RECIPE_IMAGES[dish_name]
    
    # Try case-insensitive match
    dish_name_lower = dish_name.lower()
    for key in RECIPE_IMAGES:
        if key.lower() == dish_name_lower:
            return RECIPE_IMAGES[key]
    
    # Try partial match (e.g., "Chicken tikka" matches "Chicken tikka masala")
    for key in RECIPE_IMAGES:
        if dish_name_lower in key.lower() or key.lower() in dish_name_lower:
            return RECIPE_IMAGES[key]
    
    # Return default image
    return RECIPE_IMAGES["default"]
